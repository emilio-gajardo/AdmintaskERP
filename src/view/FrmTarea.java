    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        String idTareaString = txtIdTareaBuscar.getText();
        
        if (validacionIdTarea(idTareaString) == true) {
            
            //btnAddSubTarea.setEnabled(true);
            txtIdTareaBuscar.setEnabled(false);
            String[] vector = new String[8];
            int idTareaInt = Integer.parseInt(txtIdTareaBuscar.getText());
            vector = tareaBO.buscarTarea(idTareaInt);

            txtNombreEd.setText(vector[1]);
            txtDescripcionEd.setText(vector[6]);
            String idUsuarioString = vector[7];
            String idEstadoString = vector[4];
            String idFuncionString = vector[5];
            
            String[] vectorUsuario = new String[2];
            String[] vectorEstado = new String[2];
            String[] vectorFuncion = new String[2];
            
            int idUsuarioInt = Integer.parseInt(idUsuarioString);
            int idEstadoInt = Integer.parseInt(idEstadoString);
            int idFuncionInt = Integer.parseInt(idFuncionString);
            
            vectorFuncion = funcionBO.buscarFuncion(idFuncionInt);
            String FuncionNombre = vectorFuncion[1];
            cmbFuncionEd.setSelectedItem(FuncionNombre); 
            
            vectorUsuario = usuarioBO.buscarUsuario(idUsuarioInt);
            String usuarioNombre = vectorUsuario[1];
            cmbEncargadoEd.setSelectedItem(usuarioNombre); 
            
            vectorEstado = estadoBO.buscarEstado(idEstadoInt);
            String EstadoNombre = vectorEstado[1];
            cmbEstadoEd.setSelectedItem(EstadoNombre); 
            
            try {
                String fechaInicioString = vector[2];
                SimpleDateFormat fechaSimpleDate = new SimpleDateFormat("yyyy-MM-dd");
                Date fechaInicioDate = fechaSimpleDate.parse(fechaInicioString);
                jDateFechaInicioEd.setDate(fechaInicioDate);
                
                String fechaTerminoString = vector[3];
                Date fechaTerminoDate = fechaSimpleDate.parse(fechaTerminoString);
                jDateFechaTerminoEd.setDate(fechaTerminoDate);
                
            } catch (ParseException ex) {
                //System.out.println("Fecha en formato incorrecto!");
                Logger.getLogger(FrmCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            limpiarCampos();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

