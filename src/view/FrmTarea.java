    private boolean validacionIdTarea(String idTareaString) {
        
        if (Validar.campoVacio(idTareaString) == false) {
            mostrarMensaje("Error: \nEl campo Id Tarea est谩 vac铆o! \nDebe completar el campo.");
            txtIdTareaBuscar.grabFocus();
            limpiarCampos();
            return false;
        }

        if (Validar.numeroEntero(idTareaString) == false) {
            mostrarMensaje("Error: \nEl dato ingresado debe ser num茅rico!");
            txtIdTareaBuscar.grabFocus();
            limpiarCampos();
            return false;
        }

        int idTareaInt = Integer.parseInt(idTareaString);
        String[] vector = new String[2];
        vector = tareaBO.buscarTarea(idTareaInt);
        String idTarea = vector[0];

        if (idTarea == "") {
            mostrarMensaje("Error: \nEl id ingresado no existe!");
            txtIdTareaBuscar.grabFocus();
            return false;
        }
        return true;
    }
    
