    private boolean validacionAgregar(String descripcion) {

        //Validación de los campos no estén vacíos o solo con espacios en blanco
        if (Validar.campoVacio(descripcion) == false) {
            mostrarMensaje("Error: \nLa descripcion está vacía! \nDebe completar el campo.");
            txtDescripcion.grabFocus(); //Dar el enfoque al control
            return false;
        }

        if (descripcion.equals("Administrador") || descripcion.equals("administrador")) {
            mostrarMensaje("Error: \nEl rol de administrador ya existe!");
            txtDescripcion.grabFocus(); //Dar el enfoque al control
            return false;
        }

        return true;
    }

    private boolean validacionEdicion(String descripcionEd, String idRolString, int idRolInt) {

        //Validacion del id como campo vacio
        /*if (idRolString.equals("") || idRolString.isEmpty() || idRolInt == 0) {
            mostrarMensaje("Error: \nEl campo id está vacio!");
            txtIdRolBuscar.grabFocus();
            return false;
        }*/
        
        if (Validar.numeroEntero(idRolString)== false){
            mostrarMensaje("Error: \nEl campo id está vacio!");
            txtIdRolBuscar.grabFocus();
            return false;
        }

        //Validación de los campos no estén vacíos o solo con espacios en blanco
        if (Validar.campoVacio(descripcionEd) == false) {
            mostrarMensaje("Error: \nLa descripcion está vacía! \nDebe completar el campo.");
            txtDescripcionEd.grabFocus(); //Dar el enfoque al control
            return false;
        }

        //Validacion de rol Administrador
        if (idRolInt == 1 || descripcionEd.equals("Administrador") || descripcionEd.equals("administrador")) {
            mostrarMensaje("Error: \nPor seguridad! \nEl rol de 'Administrador' no es editable!");
            limpiarCampos();
            txtDescripcionEd.grabFocus(); //Dar el enfoque al control
            return false;
        }
        return true;
    }

