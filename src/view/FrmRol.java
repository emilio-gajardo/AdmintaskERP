    private boolean validacionIdRol(String idRolString) {

        //Validar que el campo de busqueda no esté vacío
        if (Validar.campoVacio(idRolString) == false) {
            mostrarMensaje("Error: \nEl campo está vacío! \nDebe completar el campo.");
            txtIdRolBuscar.grabFocus();
            limpiarCampos();
            return false;
        }

        //Validar que el campo de busqueda sea numérico
        if (Validar.numeroEntero(idRolString) == false) {
            mostrarMensaje("Error: \nEl dato ingresado debe ser numérico!");
            txtIdRolBuscar.grabFocus();
            limpiarCampos();
            return false;
        }

        //Validar y restringir el id del administrador
        if (idRolString.equals("1")) {
            mostrarMensaje("Error: \nPor seguridad \nEl rol de 'Administrador' no es editable!");
            txtIdRolBuscar.grabFocus();
            limpiarCampos();
            return false;
        }

        //Validar que id exista
        //RolBO rolBO = new RolBO();
        int idRolInt = Integer.parseInt(idRolString);
        String[] vector = new String[2];
        vector = rolBO.buscarRol(idRolInt);
        String idRol = vector[0];

        if (idRol == "") {
            mostrarMensaje("Error: \nEl id ingresado no existe!");
            txtIdRolBuscar.grabFocus();
            limpiarCampos();
            return false;
        }
        return true;
    }

